public class Java_operators {
    public static void main(String[] args)
    {
        int dd = 4;
        int e = 6 % dd;
        System.out.println(e);
        System.out.println(64>8 && 64>9);
        System.out.println(64>8 || 64>9);

        // practice examples
        // #1
        int x=7;
        int y=1;
        int k= x-y/2;
        System.out.println(k);

        // # 2
        int a = 4;
        int b = 4;
        int c = 1;
        int l= (b*b-4*a*c)/(2*a);
        System.out.println(l);

        // #3
        int u=1;
        int v=2;
        int m= v*v-u*u;
        System.out.println(m);

        // #4
        int j= 3;
        int p=1;
        int d=2;
        int n = j*p-d;
        System.out.println(n);



    }
}
